package com.ug.model;

/**
 * @author srini
 *
 */
public class User {

	private String userName;
	private String classifiedLink;
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the classifiedLink
	 */
	public String getClassifiedLink() {
		return classifiedLink;
	}
	/**
	 * @param classifiedLink the classifiedLink to set
	 */
	public void setClassifiedLink(String classifiedLink) {
		this.classifiedLink = classifiedLink;
	}
	
	
}
